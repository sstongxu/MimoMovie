def findSearchPage(keywords):
    '''获取网页总页数'''
    sub = subtitle_db()
    host = "https://www.zimuku.org/search?q="
    resp = requests.post(host + keywords, headers)
    selector = BeautifulSoup(resp.text, 'html.parser')
    dic = []
    for video_url in selector.find_all('tr'):
        if video_url.get('class') != None and video_url.a.get('title')!=None and keywords in video_url.a.get('title') and "even" in video_url.get('class') or "odd" in video_url.get('class'):
            a = video_url.a.get('href').replace("detail", "dld")
            b = video_url.a.get('title')
            dic.append("https://www.zimuku.org"+a)
            sub.addNameInformation(keywords="https://www.zimuku.org"+a, subtitlename=b)
    print("列表获取完成")
    sub.close_connect()
    return dic

